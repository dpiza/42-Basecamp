void	ft_rev_int_tab(int *tab, int size)
{
	int num[size];
	int i;

	i = 0;
	while (i < size)
	{
		num[i] = tab[size - 1 - i];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = num[i];
		i++;
	}
}
