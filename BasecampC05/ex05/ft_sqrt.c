int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (0);
	while (i * i != nb)
	{
		if (i > 46341)
			return (0);
		if (i * i > nb)
			return (0);
		i++;
	}
	return (i);
}
