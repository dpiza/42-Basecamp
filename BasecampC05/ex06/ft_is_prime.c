int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 1)
		return (0);
	while (nb % i != 0)
	{
		if (i > nb)
			return (0);
		i++;
	}
	if (nb == i)
		return (1);
	else
		return (0);
}
